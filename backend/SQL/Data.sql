DROP DATABASE IF EXISTS WFH_Scheduling;
CREATE DATABASE IF NOT EXISTS WFH_Scheduling;
USE WFH_Scheduling;

-- Create Employee Table
CREATE TABLE Employee (
    Staff_ID INT PRIMARY KEY,
    Staff_FName VARCHAR(50) NOT NULL,
    Staff_LName VARCHAR(50) NOT NULL,
    Dept VARCHAR(50) NOT NULL,
    Position VARCHAR(50) NOT NULL,
    Country VARCHAR(50) NOT NULL,
    Email VARCHAR(50) NOT NULL,
    Reporting_Manager INT,
    Role INT NOT NULL,
    CONSTRAINT FK_Reporting_Manager FOREIGN KEY (Reporting_Manager)
    REFERENCES Employee(Staff_ID)
);


-- Create Request Table
CREATE TABLE Request (
    Request_ID INT PRIMARY KEY AUTO_INCREMENT,
    Staff_ID INT,
    Creation_Date DATE NOT NULL,
    Apply_Reason VARCHAR(100) NOT NULL,
    Reject_Reason VARCHAR(100),
    CONSTRAINT FK_Staff_ID FOREIGN KEY (Staff_ID)
    REFERENCES Employee(Staff_ID)
);


-- Create Request_Dates Table
CREATE TABLE Request_Dates (
    Request_Date_ID INT PRIMARY KEY AUTO_INCREMENT,
    Request_ID INT,
    Request_Date DATE NOT NULL,
    Request_Shift VARCHAR(5) NOT NULL,
    Request_Status VARCHAR(20) NOT NULL,
    Withdraw_Reason VARCHAR(100),
    Rescind_Reason VARCHAR(100),
    CONSTRAINT FK_Request_ID FOREIGN KEY (Request_ID)
    REFERENCES Request(Request_ID)
);


-- Create Status_Log Table
CREATE TABLE Status_Log (
    log_id INT PRIMARY KEY AUTO_INCREMENT,
    request_id INT,
    log_date DATE,
    action VARCHAR(100) NOT NULL,
    reason VARCHAR(100),
    CONSTRAINT FK_Request_Log FOREIGN KEY (request_id)
    REFERENCES Request(Request_ID)
);


